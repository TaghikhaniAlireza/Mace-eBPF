// Package mace is a thin Go client over the Mace-eBPF Rust core (statically linked), with channel-based delivery of structured events.
//
// Log level FFI: see mace_set_log_level in mace-ebpf/include/mace.h — wrapped by [SetLogLevel] / [InitEngineWithConfig].
package mace

